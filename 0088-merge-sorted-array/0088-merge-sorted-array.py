class Solution(object):
    def merge(self, nums1, m, nums2, n):
        """
        :type nums1: List[int]
        :type m: int
        :type nums2: List[int]
        :type n: int
        :rtype: None Do not return anything, modify nums1 in-place instead.
        """
        for item in nums2:
            indexOf_zero = nums1.index(0)
            nums1[indexOf_zero] = item
        nums1.sort()